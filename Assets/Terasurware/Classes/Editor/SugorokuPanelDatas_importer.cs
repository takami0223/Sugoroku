using UnityEngine;
using System.Collections;
using System.IO;
using UnityEditor;
using System.Xml.Serialization;
using NPOI.HSSF.UserModel;
using NPOI.XSSF.UserModel;
using NPOI.SS.UserModel;

public class SugorokuPanelDatas_importer : AssetPostprocessor {
	private static readonly string filePath = "Assets/Terasurware/Excel Data/SugorokuPanelDatas.xlsx";
	private static readonly string exportPath = "Assets/Terasurware/Excel Data/SugorokuPanelDatas.asset";
	private static readonly string[] sheetNames = { "Blue","Red", };
	
	static void OnPostprocessAllAssets (string[] importedAssets, string[] deletedAssets, string[] movedAssets, string[] movedFromAssetPaths)
	{
		foreach (string asset in importedAssets) {
			if (!filePath.Equals (asset))
				continue;
				
			Panel_Event data = (Panel_Event)AssetDatabase.LoadAssetAtPath (exportPath, typeof(Panel_Event));
			if (data == null) {
				data = ScriptableObject.CreateInstance<Panel_Event> ();
				AssetDatabase.CreateAsset ((ScriptableObject)data, exportPath);
				data.hideFlags = HideFlags.NotEditable;
			}
			
			data.sheets.Clear ();
			using (FileStream stream = File.Open (filePath, FileMode.Open, FileAccess.Read, FileShare.ReadWrite)) {
				IWorkbook book = null;
				if (Path.GetExtension (filePath) == ".xls") {
					book = new HSSFWorkbook(stream);
				} else {
					book = new XSSFWorkbook(stream);
				}
				
				foreach(string sheetName in sheetNames) {
					ISheet sheet = book.GetSheet(sheetName);
					if( sheet == null ) {
						Debug.LogError("[QuestData] sheet not found:" + sheetName);
						continue;
					}

					Panel_Event.Sheet s = new Panel_Event.Sheet ();
					s.name = sheetName;
				
					for (int i=1; i<= sheet.LastRowNum; i++) {
						IRow row = sheet.GetRow (i);
						ICell cell = null;
						
						Panel_Event.Param p = new Panel_Event.Param ();
						
					cell = row.GetCell(0); p.Sentence = (cell == null ? "" : cell.StringCellValue);
					cell = row.GetCell(1); p.Amount = (int)(cell == null ? 0 : cell.NumericCellValue);
						s.list.Add (p);
					}
					data.sheets.Add(s);
				}
			}

			ScriptableObject obj = AssetDatabase.LoadAssetAtPath (exportPath, typeof(ScriptableObject)) as ScriptableObject;
			EditorUtility.SetDirty (obj);
		}
	}
}
